<?php if ( ! defined( 'DEVIL_SYS_CORE_PATH' ) ) exit( 'No direct script access allowed' );
/**
  @brief        Transfer_process Controller ( 棑程 )
**/

class Transfer_process extends Controller
{
    /**
    @brief      建構子
    **/
    public function __construct()
    {
        # ----------------------------------------------------------------------
        # 如果不是 CLI 指令，就不執行
        # ----------------------------------------------------------------------
        if ( php_sapi_name() != 'cli' )
        {
            exit;
        }
        # ----------------------------------------------------------------------
        # 先將核心自動載入的類別實例化至當前類別中
        # ----------------------------------------------------------------------
        parent::__initialization();
    }
    /**
    | 初始化
    | --------------------------------------------------------------------------
    | @access       private
    | --------------------------------------------------------------------------
    **/
    private function _init()
    {
        # ----------------------------------------------------------------------
        # 手動載入需要的類別
        # ----------------------------------------------------------------------
        $this->Core_Loader->library( array( 'Pdo_Driver' ) );
        # ----------------------------------------------------------------------
        # 手動載入需要的模組
        # ----------------------------------------------------------------------
        $this->Core_Loader->model( array( 'KConfig' ) );
    }

    /**
    @brief      排程執行程序入口
    **/
    public function index()
    {
        # ----------------------------------------------------------------------
        # 執行初始化
        # ----------------------------------------------------------------------
        $this->_init();
        # ----------------------------------------------------------------------
        # 開始寫要做的事情...
        # ----------------------------------------------------------------------
        Core_Export( 'Hi, is Command Line Interface ( CLI ).' );
    }
}