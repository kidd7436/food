<?php if ( ! defined( 'DEVIL_SYS_CORE_PATH' ) ) exit( 'No direct script access allowed' );
/**
  @brief        News Controller ( 公告管理控制器 )
**/

class News extends Controller
{
    /**
    @brief      建構子
    **/
    public function __construct()
    {
        # ----------------------------------------------------------------------
        # 呼叫 "父類別" 的 __backstageInit Method
        # ----------------------------------------------------------------------
        $this->__backstageInit();
        # ----------------------------------------------------------------------
        # 在此處統一判斷有無 "帳號管理" , 沒權限導向到首頁
        # ----------------------------------------------------------------------
        if ( Bootstart::$_lib['My_Power']->isPower( 2 ) === FALSE )
        {
            Core_Redirect( 'Op' );
        }
        # ----------------------------------------------------------------------
        # 手動載入需要的類別
        # ----------------------------------------------------------------------
        Bootstart::$_lib[ 'Core_Loader' ]->model( array( 'News' ) );
        # ----------------------------------------------------------------------
        # 重新建構類別
        # ----------------------------------------------------------------------
        $this->__initialization();
    }

    /**
    @brief      index處理導向
    **/
    public function index( $_type = 'list', $_id = 0 )
    {
        # ----------------------------------------------------------------------
        # 過濾參數
        # ----------------------------------------------------------------------
        $id = intval( $_id );
        # ----------------------------------------------------------------------
        # 全部轉小寫，方便判斷
        # ----------------------------------------------------------------------
        switch( strtolower( $_type ) )
        {
            # ------------------------------------------------------------------
            # 預設列表畫面
            # ------------------------------------------------------------------
            case 'list':
                self::_datalist( );
                break;
            # ------------------------------------------------------------------
            # 新增
            # ------------------------------------------------------------------
            case 'add':
            # ------------------------------------------------------------------
            # 修改
            # ------------------------------------------------------------------
            case 'edit':
                self::_add( $id );
                break;
            # ------------------------------------------------------------------
            # 修改處理程序
            # ------------------------------------------------------------------
            case 'post':
                    self::_post( );
                    break;
            # ------------------------------------------------------------------
            # 刪除
            # ------------------------------------------------------------------
            case 'del':
                self::_del( $id );
                break;
        }
    }

    /**
    @brief      加熱即食包( 首頁 )
    **/
    public function _datalist(  )
    {
        # ----------------------------------------------------------------------
        # 初始化變數
        # ----------------------------------------------------------------------
        $list = "";
        # ----------------------------------------------------------------------
        # 取出所有餐盒資料
        # ----------------------------------------------------------------------
        $dataArr =  Bootstart::$_mod[ 'News_Model' ]->getNewsAll( );
        # ----------------------------------------------------------------------
        # 防呆 有資料才做
        # ----------------------------------------------------------------------
        if( $dataArr )
        {
            # ------------------------------------------------------------------
            # 產生 Table
            # ------------------------------------------------------------------
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_table( '' , 'table table-striped table-bordered table-hover table-checkable align-center' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_TSection( 'thead' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_row();
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '#' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '標題' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '內容' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '開始顯示' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '結速顯示' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '功能' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_TSection( 'tbody' );
            # ------------------------------------------------------------------
            # tag狀態
            # ------------------------------------------------------------------
            $enabled = array(
                '0' => '<span class="label label-danger">停用</span>' ,
                '1' => '<span class="label label-success">啟用</span>'
            );
            # ------------------------------------------------------------------
            # 產生tr
            # ------------------------------------------------------------------
            foreach( $dataArr as $key => $val )
            {
                # --------------------------------------------------------------
                # 產生功能按鈕
                # --------------------------------------------------------------
                $edit = '<a class="btn btn-xs bs-tooltip" href="'.DEVIL_APP_Url.__CLASS__."/index/edit/".$val['id'].'" title="修改"><i class=" icon-edit"></i></a>&nbsp;&nbsp;'
                      . '<button class="btn btn-xs bs-tooltip btn-danger" title="Delete" onClick="del('.$val['id'].')"><i class="icon-trash"></i></button>';
                # --------------------------------------------------------------
                # tr
                # --------------------------------------------------------------
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_row();
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( $val[ 'id' ] );
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( $val[ 'title' ] );
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( $val[ 'content' ] );
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( date( "Y-m-d H:i:s", $val[ 'dts' ] ) );
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( date( "Y-m-d H:i:s", $val[ 'dte' ] ) );
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( $edit );
            }
            # ------------------------------------------------------------------
            # 產生所有設定的表格資料
            # ------------------------------------------------------------------
            $list = Bootstart::$_lib[ 'Core_HtmlTable' ]->generate();
        }
        else
        {
            $list = '<div class="alert alert-info">
                        <strong>無任何資料！！</strong>
                    </div>';
        }
        # ----------------------------------------------------------------------
        # 輸出內容
        # ----------------------------------------------------------------------
        $this->My_HtmlView->Extension_HtmlView
        (
            __CLASS__.".Page" ,
            __CLASS__.".html" ,
            array
            (
                # --------------------------------------------------------------
                # 站名
                # --------------------------------------------------------------
                "TITLE" => DEVIL_APP_PROJECT_NAME ,
                # --------------------------------------------------------------
                # JS、CSS、圖片等的存取網址
                # --------------------------------------------------------------
                "PUBLIC_URL" => DEVIL_APP_PUBLIC_URL ,
                # --------------------------------------------------------------
                # 當前網址
                # --------------------------------------------------------------
                "URL" => DEVIL_APP_Url ,
                # --------------------------------------------------------------
                # 資料
                # --------------------------------------------------------------
                "LIST" => $list ,
                # --------------------------------------------------------------
                # 新增商品連結
                # --------------------------------------------------------------
                "ADD" => DEVIL_APP_Url . __CLASS__ . '/index/add/',
                # --------------------------------------------------------------
                # 刪除商品連結
                # --------------------------------------------------------------
                "DEL" => DEVIL_APP_Url . __CLASS__ . '/index/del/',
            )
        );
    }

    /**
    @brief      公告管理( 新增、修改頁面 )
    **/
    private function _add( $_id )
    {
        # ----------------------------------------------------------------------
        # 初始化
        # ----------------------------------------------------------------------
        $htmlArr[ "title" ] = "";
        $htmlArr[ "content" ] = "";
        $htmlArr[ "enabled0" ] = "";
        $htmlArr[ "enabled1" ] = "checked";
        $htmlArr[ "dts" ] = "";
        $htmlArr[ "dte" ] = "";
        $htmlArr[ "HIDDEN_ID" ] = "";
        $htmlArr[ "button" ] = "新增";
        # ----------------------------------------------------------------------
        # 這裡處理修改
        # ----------------------------------------------------------------------
        if( $_id )
        {
            # ------------------------------------------------------------------
            # 初始化
            # ------------------------------------------------------------------
            $dataArr = array();
            # ------------------------------------------------------------------
            # 取出指定的餐盒資料
            # ------------------------------------------------------------------
            $dataArr =  Bootstart::$_mod[ 'News_Model' ]->getNewsById( $_id );
            # ------------------------------------------------------------------
            # 防呆
            # ------------------------------------------------------------------
            if( ! $dataArr )
            {
                show_errormsg( "錯誤的編號！！" );
            }
            # ------------------------------------------------------------------
            # 商品資料
            # ------------------------------------------------------------------
            $htmlArr = $dataArr;
            # ------------------------------------------------------------------
            # 處理『狀態』
            # ------------------------------------------------------------------
            $htmlArr[ "enabled0" ] = ( ( $dataArr[ "enabled" ] == 0 ) ? 'checked' : "" );
            $htmlArr[ "enabled1" ] = ( ( $dataArr[ "enabled" ] == 1 ) ? 'checked' : "" );
            # ------------------------------------------------------------------
            # 處理『日期的轉換』
            # ------------------------------------------------------------------
            $htmlArr[ "dts" ] = date( "Y-m-d", $dataArr[ "dts" ] );
            $htmlArr[ "dte" ] = date( "Y-m-d", $dataArr[ "dte" ] );
            # ------------------------------------------------------------------
            # ID
            # ------------------------------------------------------------------
            $htmlArr[ "HIDDEN_ID" ] = '<input type="hidden" name="id" value="'.$dataArr[ "id" ].'" />';
            # ------------------------------------------------------------------
            # 按鈕文字
            # ------------------------------------------------------------------
            $htmlArr[ "button" ] = "修改";
        }
        $htmlArr[ "TITLE" ] = DEVIL_APP_PROJECT_NAME;
        $htmlArr[ "PUBLIC_URL" ] = DEVIL_APP_PUBLIC_URL;
        $htmlArr[ "URL" ] = DEVIL_APP_Url;
        $htmlArr[ "POST_URL" ] = DEVIL_APP_Url . __CLASS__ . '/index/post/';
        # ----------------------------------------------------------------------
        # 輸出內容
        # ----------------------------------------------------------------------
        $this->My_HtmlView->Extension_HtmlView
        (
            __CLASS__."_Add_Page" ,
            __CLASS__."_Add.html" ,
            $htmlArr
        );
    }

    /**
    @brief      加熱即食包( 新增、俢改程序 )
    **/
    private function _post( )
    {
        if ( Core_IsPost() && $_POST != '' )
        {
            # ------------------------------------------------------------------
            # 初始化變數
            # ------------------------------------------------------------------
            $id = $title = $money = $discount = '';
            # ------------------------------------------------------------------
            # 判斷是修改 還是 新增
            # ------------------------------------------------------------------
            if( isset( $_POST[ 'id' ] ) )
            {
                # --------------------------------------------------------------
                # 過濾『ID』
                # --------------------------------------------------------------
                $id = Bootstart::$_lib[ 'Core_Input' ]->post( 'id' , 'int' );
                # --------------------------------------------------------------
                # 錯誤輸出
                # --------------------------------------------------------------
                if ( ! $id  )
                {
                    show_errormsg( '重大錯誤！！' );
                }
            }
            # ------------------------------------------------------------------
            # 過濾『TITLE』
            # ------------------------------------------------------------------
            if( $_POST[ 'title' ] == "" )
            {
                show_errormsg( '請輸入標題！！' );
            }
            else
            {
                $title = $_POST[ "title" ];
            }
            # ------------------------------------------------------------------
            # 過濾『content』
            # ------------------------------------------------------------------
            if( $_POST[ 'content' ] == "" )
            {
                show_errormsg( '請輸入內容！！' );
            }
            else
            {
                $content = $_POST[ "content" ];
            }
            # ------------------------------------------------------------------
            # 過濾『開始顯示時間』
            # ------------------------------------------------------------------
            if( $_POST[ 'dts' ] == "" )
            {
                show_errormsg( '請輸入開始顯示時間！！' );
            }
            else
            {
                $dts = strtotime( $_POST[ "dts" ] . "00:00:00" );
            }
            # ------------------------------------------------------------------
            # 過濾『結束顯示時間』
            # ------------------------------------------------------------------
            if( $_POST[ 'dte' ] == "" )
            {
                show_errormsg( '請輸入結束顯示時間！！' );
            }
            else
            {
                $dte = strtotime( $_POST[ "dte" ] . "23:59:59" );
            }
            # ------------------------------------------------------------------
            # 防呆
            # ------------------------------------------------------------------
            if( $dts > $dte )
            {
                show_errormsg( '開始時間不得大於結束時間，請重新輸入！！' );
            }
            # ------------------------------------------------------------------
            # 組成資訊
            # ------------------------------------------------------------------
            $dataArr = array();
            $dataArr[ 'title' ] = $title;
            $dataArr[ 'content' ] = $content;
            $dataArr[ 'dts' ] = $dts;
            $dataArr[ 'dte' ] = $dte;
            $dataArr[ "updateid" ] = Core_LoadSession( 'id' );
            $dataArr[ "updatedt" ] = date('Y-m-d H:i:s');
            $dataArr[ "updateip" ] = sprintf( "%u" , ip2long ( Bootstart::$_lib['Core_UserAgent']->ip ) );
            # ------------------------------------------------------------------
            # 如果有帶 ID 代表是修改
            # ------------------------------------------------------------------
            if( $id )
            {
                if( Bootstart::$_lib[ 'Core_Pdo_Driver' ]->db_update( 'news' , $dataArr , "id = {$id}" ) === FALSE )
                {
                    show_errormsg( '修改失敗，請重新輸入！！' );
                }
            }
            # ------------------------------------------------------------------
            # 新增這裡處理
            # ------------------------------------------------------------------
            else
            {
                if( Bootstart::$_lib[ 'Core_Pdo_Driver' ]->db_insert( 'news' , $dataArr ) === FALSE )
                {
                    show_errormsg( '新增失敗，請重新輸入！！' );
                }
            }
            Core_Redirect( __CLASS__ );
        }
        Core_Redirect( __CLASS__ );
    }

    /**
    @brief      加熱即食包( 刪除 )
    **/
    private function _del( $_id )
    {
        # ----------------------------------------------------------------------
        # 過濾『ID』
        # ----------------------------------------------------------------------
        $id = intval( $_id );
        # ----------------------------------------------------------------------
        # 防呆
        # ----------------------------------------------------------------------
        if( $id )
        {
            # ------------------------------------------------------------------
            # 執行更新
            # ------------------------------------------------------------------
            if ( Bootstart::$_lib[ 'Core_Pdo_Driver' ]->query( "DELETE FROM `news` WHERE `id` = '{$id}'" ) === FALSE )
            {
                show_errormsg( '刪除失败！' );
            }
        }
        Core_Redirect( __CLASS__ );
    }
}