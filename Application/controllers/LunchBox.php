<?php if ( ! defined( 'DEVIL_SYS_CORE_PATH' ) ) exit( 'No direct script access allowed' );
/**
  @brief        LunchBox Controller ( 精緻餐盒控制器 )
**/

class LunchBox extends Controller
{
    private $type = "";
    private $weekday = array();
    private $imgurl = '';
    /**
    @brief      建構子
    **/
    public function __construct()
    {
        # ----------------------------------------------------------------------
        # 呼叫 "父類別" 的 __backstageInit Method
        # ----------------------------------------------------------------------
        $this->__backstageInit();
        # ----------------------------------------------------------------------
        # 在此處統一判斷有無 "帳號管理" , 沒權限導向到首頁
        # ----------------------------------------------------------------------
        if ( Bootstart::$_lib['My_Power']->isPower( 3 ) === FALSE )
        {
            Core_Redirect( 'Op' );
        }
        # ----------------------------------------------------------------------
        # 手動載入需要的類別
        # ----------------------------------------------------------------------
        Bootstart::$_lib[ 'Core_Loader' ]->model( array( 'Product' ) );
        # ----------------------------------------------------------------------
        # 後台圖片存檔路徑
        # ----------------------------------------------------------------------
        $this->imgurl = DEVIL_SYSTEM_PATH . DIRECTORY_SEPARATOR . implode( DIRECTORY_SEPARATOR , array( "Player_Area" , "public" , "images" ,"product" ) ) . DIRECTORY_SEPARATOR;
        # ----------------------------------------------------------------------
        # 類型
        # ----------------------------------------------------------------------
        $this->type = 1;
        # ----------------------------------------------------------------------
        # 星期
        # ----------------------------------------------------------------------
        $this->weekday = array( 1 => "星期一", 2 => "星期二", 3 => "星期三 ", 4 => "星期四", 5 => "星期五", 6 => "星期六", 7 => "星期日" );
        # ----------------------------------------------------------------------
        # 重新建構類別
        # ----------------------------------------------------------------------
        $this->__initialization();
    }

    /**
    @brief      index處理導向
    **/
    public function index( $_type = 'list', $_id = 0 )
    {
        # ----------------------------------------------------------------------
        # 過濾參數
        # ----------------------------------------------------------------------
        $id = intval( $_id );
        # ----------------------------------------------------------------------
        # 全部轉小寫，方便判斷
        # ----------------------------------------------------------------------
        switch( strtolower( $_type ) )
        {
            # ------------------------------------------------------------------
            # 預設列表畫面
            # ------------------------------------------------------------------
            case 'list':
                self::_datalist( );
                break;
            # ------------------------------------------------------------------
            # 新增
            # ------------------------------------------------------------------
            case 'add':
            # ------------------------------------------------------------------
            # 修改
            # ------------------------------------------------------------------
            case 'edit':
                self::_add( $id );
                break;
            # ------------------------------------------------------------------
            # 修改處理程序
            # ------------------------------------------------------------------
            case 'post':
                    self::_post( );
                    break;
            # ------------------------------------------------------------------
            # 上傳圖片
            # ------------------------------------------------------------------
            case 'upd_logo':
                self::_upd_logo( $id );
                break;
            # ------------------------------------------------------------------
            # 圖片上傳程序
            # ------------------------------------------------------------------
            case 'upd':
                self::_upd( );
                break;
            # ------------------------------------------------------------------
            # 刪除
            # ------------------------------------------------------------------
            case 'del':
                self::_del( $id );
                break;
        }
    }

    /**
    @brief      精緻餐盒( 首頁 )
    **/
    public function _datalist( )
    {
        # ----------------------------------------------------------------------
        # 初始化變數
        # ----------------------------------------------------------------------
        $list = "";
        # ----------------------------------------------------------------------
        # 取出所有餐盒資料
        # ----------------------------------------------------------------------
        $dataArr =  Bootstart::$_mod[ 'Product_Model' ]->getProductByType( $this->type );
        # ----------------------------------------------------------------------
        # 防呆 有資料才做
        # ----------------------------------------------------------------------
        if( $dataArr )
        {
            # ------------------------------------------------------------------
            # 產生 Table
            # ------------------------------------------------------------------
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_table( '' , 'table table-striped table-bordered table-hover table-checkable align-center' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_TSection( 'thead' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_row();
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '品名' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '建議售價' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '促銷價' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '點餐日期' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '狀態' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( '功能' , 'th' );
            Bootstart::$_lib[ 'Core_HtmlTable' ]->add_TSection( 'tbody' );
            # ------------------------------------------------------------------
            # tag狀態
            # ------------------------------------------------------------------
            $enabled = array(
                '0' => '<span class="label label-danger">停用</span>' ,
                '1' => '<span class="label label-success">啟用</span>'
            );
            # ------------------------------------------------------------------
            # 產生tr
            # ------------------------------------------------------------------
            foreach( $dataArr as $key => $val )
            {
                # --------------------------------------------------------------
                # 初始化
                # --------------------------------------------------------------
                $imgPath  = $imgType = $imgData = $imgBase64 = $img = "";
                # --------------------------------------------------------------
                # 取得圖檔路徑
                # --------------------------------------------------------------
                $imgPath = $this->imgurl . $val[ 'id' ].".jpg";
                $imgType = pathinfo( $imgPath , PATHINFO_EXTENSION );
                $img  = "";
                if( is_file( $imgPath ) )
                {
                    $imgData = file_get_contents( $imgPath );
                    $imgBase64 = 'data:image/' . $imgType . ';base64,' . base64_encode( $imgData );
                    $img = '<img src="'. $imgBase64 .'">';
                }
                # --------------------------------------------------------------
                # 產生功能按鈕
                # --------------------------------------------------------------
                $edit = '<a class="btn btn-xs bs-tooltip" rel="popover" data-img="'.$imgBase64.'"><i class="icon-eye-open"></i></a>&nbsp;&nbsp;'
                      . '<a class="btn btn-xs bs-tooltip" href="'.DEVIL_APP_Url.__CLASS__."/index/edit/".$val['id'].'" title="修改"><i class="icon-edit"></i></a>&nbsp;&nbsp;'
                      . '<a class="btn btn-xs bs-tooltip" href="'.DEVIL_APP_Url.__CLASS__."/index/upd_logo/".$val['id'].'" title="上傳圖片"><i class="icon-cloud-upload"></i></a>&nbsp;&nbsp;'
                      . '<button class="btn btn-xs bs-tooltip btn-danger" title="Delete" onClick="del('.$val['id'].')"><i class="icon-trash"></i></button>';
                # --------------------------------------------------------------
                # tr
                # --------------------------------------------------------------
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_row();
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( $val[ 'title' ] );
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( $val[ 'money' ] );
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( $val[ 'discount' ] );
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( $val[ 'peddledt' ] );
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( $enabled[ $val[ 'enabled' ] ] );
                Bootstart::$_lib[ 'Core_HtmlTable' ]->add_cell( $edit );
            }
            # ------------------------------------------------------------------
            # 產生所有設定的表格資料
            # ------------------------------------------------------------------
            $list = Bootstart::$_lib[ 'Core_HtmlTable' ]->generate();
        }
        else
        {
            $list = '<div class="alert alert-info alert-dismissible">
                        <h5><i class="icon fas fa-info"></i> 無任何資料！！</h5>
                    </div>';
        }
        # ----------------------------------------------------------------------
        # 輸出內容
        # ----------------------------------------------------------------------
        $this->My_HtmlView->Extension_HtmlView
        (
            __CLASS__.".Page" ,
            __CLASS__.".html" ,
            array
            (
                # --------------------------------------------------------------
                # 站名
                # --------------------------------------------------------------
                "TITLE" => DEVIL_APP_PROJECT_NAME,
                # --------------------------------------------------------------
                # JS、CSS、圖片等的存取網址
                # --------------------------------------------------------------
                "PUBLIC_URL" => DEVIL_APP_PUBLIC_URL,
                # --------------------------------------------------------------
                # 當前網址
                # --------------------------------------------------------------
                "URL" => DEVIL_APP_Url,
                # --------------------------------------------------------------
                # 資料
                # --------------------------------------------------------------
                "LIST" => $list,
                # --------------------------------------------------------------
                # 新增商品連結
                # --------------------------------------------------------------
                "ADD" => DEVIL_APP_Url . __CLASS__ . '/index/add/',
                # --------------------------------------------------------------
                # 刪除商品連結
                # --------------------------------------------------------------
                "DEL" => DEVIL_APP_Url . __CLASS__ . '/index/del/',
            )
        );
    }

    /**
    @brief      精緻餐盒( 新增、修改頁面 )
    **/
    private function _add( $_id )
    {
        # ----------------------------------------------------------------------
        # 初始化
        # ----------------------------------------------------------------------
        $htmlArr[ "title" ] = "";
        $htmlArr[ "money" ] = "";
        $htmlArr[ "discount" ] = "";
        $htmlArr[ "content" ] = "";
        $htmlArr[ "enabled0" ] = "";
        $htmlArr[ "enabled1" ] = "checked='checked'";
        $htmlArr[ "HIDDEN_ID" ] = "";
        $htmlArr[ "button" ] = "新增";
        $htmlArr[ "sort" ] = "";
        $weekDay = "";
        $peddledt = array();
        # ----------------------------------------------------------------------
        # 這裡處理修改
        # ----------------------------------------------------------------------
        if( $_id )
        {
            # ------------------------------------------------------------------
            # 初始化
            # ------------------------------------------------------------------
            $dataArr = array();
            # ------------------------------------------------------------------
            # 取出指定的餐盒資料
            # ------------------------------------------------------------------
            $dataArr =  Bootstart::$_mod[ 'Product_Model' ]->getProductById( $_id );
            # ------------------------------------------------------------------
            # 防呆
            # ------------------------------------------------------------------
            if( ! $dataArr )
            {
                show_errormsg( "錯誤的編號！！" );
            }
            # ------------------------------------------------------------------
            # 商品資料
            # ------------------------------------------------------------------
            $htmlArr = $dataArr;
            # ------------------------------------------------------------------
            # 處理『販賣日期』
            # ------------------------------------------------------------------
            $peddledt = explode( ",", $dataArr[ "peddledt" ] );
            # ------------------------------------------------------------------
            # 處理『狀態』
            # ------------------------------------------------------------------
            $htmlArr[ "enabled0" ] = ( ( $dataArr[ "enabled" ] == 0 ) ? 'checked="checked"' : "" );
            $htmlArr[ "enabled1" ] = ( ( $dataArr[ "enabled" ] == 1 ) ? 'checked="checked"' : '' );
            # ------------------------------------------------------------------
            # ID
            # ------------------------------------------------------------------
            $htmlArr[ "HIDDEN_ID" ] = '<input type="hidden" name="id" value="'.$dataArr[ "id" ].'" />';
            # ------------------------------------------------------------------
            # 按鈕文字
            # ------------------------------------------------------------------
            $htmlArr[ "button" ] = "修改";
        }
        # ----------------------------------------------------------------------
        # 處理星期選單
        # ----------------------------------------------------------------------
        foreach( $this->weekday as $kw => $vw )
        {
            $weekDay .= '<label class="checkbox-inline">'
                     . '<input type="checkbox" class="uniform" name="peddledt[]" value="'.$kw.'" '.( in_array( $kw, $peddledt ) ? "checked" : "" ).'>'.$vw.''
                     .'</label>';
        }
        # ----------------------------------------------------------------------
        # HTML
        # ----------------------------------------------------------------------
        $htmlArr[ "TITLE" ] = DEVIL_APP_PROJECT_NAME;
        $htmlArr[ "PUBLIC_URL" ] = DEVIL_APP_PUBLIC_URL;
        $htmlArr[ "URL" ] = DEVIL_APP_Url;
        $htmlArr[ "POST_URL" ] = DEVIL_APP_Url . __CLASS__ . '/index/post/';
        $htmlArr[ "UPD_URL" ] = DEVIL_APP_Url . __CLASS__ . '/index/upd/';
        $htmlArr[ "WEEKDAY" ] = $weekDay;
        # ----------------------------------------------------------------------
        # 輸出內容
        # ----------------------------------------------------------------------
        $this->My_HtmlView->Extension_HtmlView
        (
            __CLASS__."_Add_Page" ,
            __CLASS__."_Add.html" ,
            $htmlArr
        );
    }

    /**
    @brief      精緻餐盒( 新增、俢改程序 )
    **/
    private function _post( )
    {
        if ( Core_IsPost() && $_POST != '' )
        {
            # ------------------------------------------------------------------
            # 初始化變數
            # ------------------------------------------------------------------
            $id = $title = $money = $discount = $peddledt = $content = '';
            $sort = 1;
            # ------------------------------------------------------------------
            # 判斷是修改 還是 新增
            # ------------------------------------------------------------------
            if( isset( $_POST[ 'id' ] ) )
            {
                # --------------------------------------------------------------
                # 過濾『ID』
                # --------------------------------------------------------------
                $id = Bootstart::$_lib[ 'Core_Input' ]->post( 'id' , 'int' );
                # --------------------------------------------------------------
                # 錯誤輸出
                # --------------------------------------------------------------
                if ( ! $id  )
                {
                    show_errormsg( '重大錯誤！！' );
                }
            }
            # ------------------------------------------------------------------
            # 過濾『TITLE』
            # ------------------------------------------------------------------
            if( $_POST[ 'title' ] == "" )
            {
                show_errormsg( '請輸入品名！！' );
            }
            else
            {
                $title = $_POST[ "title" ];
            }
            # ------------------------------------------------------------------
            # 過濾『MONEY』
            # ------------------------------------------------------------------
            if( $_POST[ 'money' ] == "" )
            {
                show_errormsg( '請輸入建議售價！！' );
            }
            else
            {
                $money = Bootstart::$_lib[ 'Core_Input' ]->post( 'money' , 'int' );
                # --------------------------------------------------------------
                # 錯誤輸出
                # --------------------------------------------------------------
                if ( ! $money  )
                {
                    show_errormsg( Bootstart::$_lib[ 'Core_Input' ]->getRuleMsg( 'int' ) );
                }
            }
            # ------------------------------------------------------------------
            # 非必填 檢查有送資料在檢查格式
            # ------------------------------------------------------------------
            if( !empty( $_POST[ 'discount' ] ) )
            {
                # --------------------------------------------------------------
                # 過濾『DISCOUNT』
                # --------------------------------------------------------------
                $discount = Bootstart::$_lib[ 'Core_Input' ]->post( 'discount' , 'int' );
                # --------------------------------------------------------------
                # 錯誤輸出
                # --------------------------------------------------------------
                if ( ! $discount  )
                {
                    show_errormsg( Bootstart::$_lib[ 'Core_Input' ]->getRuleMsg( 'int' ) );
                }
            }
            # ------------------------------------------------------------------
            # 過濾『販賣日期』
            # ------------------------------------------------------------------
            if( ! isset( $_POST[ 'peddledt' ] ) || $_POST[ 'peddledt' ] == "" )
            {
                show_errormsg( '請勾選販賣日期！！' );
            }
            else
            {
                $peddledt = implode( ',' , $_POST[ 'peddledt' ] );
            }
            # ------------------------------------------------------------------
            # 過濾『排序』
            # ------------------------------------------------------------------
            $sort = Bootstart::$_lib[ 'Core_Input' ]->post( 'sort' , 'int' );
            # ------------------------------------------------------------------
            # 過濾『CONTENT』
            # ------------------------------------------------------------------
            $content = htmlspecialchars( $_POST[ "content" ] );
            # ------------------------------------------------------------------
            # 過濾『狀態』
            # ------------------------------------------------------------------
            $enabled = Bootstart::$_lib[ 'Core_Input' ]->post( 'enabled' , 'int' );
            $enabled = ( ( $enabled == false ) ? 0 :  intval( $enabled ) );
            # ------------------------------------------------------------------
            # 組成資訊
            # ------------------------------------------------------------------
            $dataArr = array();
            $dataArr[ 'type' ] = $this->type;
            $dataArr[ 'title' ] = $title;
            $dataArr[ 'money' ] = $money;
            $dataArr[ 'discount' ] =( ( $discount ) ? $discount : $money );
            $dataArr[ 'enabled' ] = $enabled;
            $dataArr[ 'peddledt' ] = $peddledt;
            $dataArr[ 'content' ] = $content;
            $dataArr[ 'sort' ] = $sort;
            $dataArr[ "updateid" ] = Core_LoadSession( 'id' );
            $dataArr[ "updatedt" ] = date('Y-m-d H:i:s');
            $dataArr[ "updateip" ] = sprintf( "%u" , ip2long ( Bootstart::$_lib['Core_UserAgent']->ip ) );
            # ------------------------------------------------------------------
            # 如果有帶 ID 代表是修改
            # ------------------------------------------------------------------
            if( $id )
            {
                if( Bootstart::$_lib[ 'Core_Pdo_Driver' ]->db_update( 'product' , $dataArr , "id = {$id}" ) === FALSE )
                {
                    show_errormsg( '修改失敗，請重新輸入！！' );
                }
            }
            # ------------------------------------------------------------------
            # 新增這裡處理
            # ------------------------------------------------------------------
            else
            {
                if( Bootstart::$_lib[ 'Core_Pdo_Driver' ]->db_insert( 'product' , $dataArr ) === FALSE )
                {
                    show_errormsg( '新增失敗，請重新輸入！！' );
                }
            }
            # ------------------------------------------------------------------
            # 寫檔
            # ------------------------------------------------------------------
            Bootstart::$_mod[ 'Product_Model' ]->writeTxt();
            # ------------------------------------------------------------------
            # 返回
            # ------------------------------------------------------------------
            Core_Redirect( __CLASS__ );
        }
        Core_Redirect( __CLASS__ );
    }

    /**
    @brief      精緻餐盒( 上傳logo頁面 )
    **/
    private function _upd_logo( $_id )
    {
        # ----------------------------------------------------------------------
        # 過濾
        # ----------------------------------------------------------------------
        $id = $_id;
        # ----------------------------------------------------------------------
        # 初始化
        # ----------------------------------------------------------------------
        $dataArr = array();
        # ----------------------------------------------------------------------
        # 取出指定的餐盒資料
        # ----------------------------------------------------------------------
        $dataArr =  Bootstart::$_mod[ 'Product_Model' ]->getProductById( $id );
        # ----------------------------------------------------------------------
        # 防呆
        # ----------------------------------------------------------------------
        if( !$dataArr )
        {
            show_errormsg( '查無此商品！！' );
        }
        # ----------------------------------------------------------------------
        # 商品資料
        # ----------------------------------------------------------------------
        $htmlArr = $dataArr;
        # ----------------------------------------------------------------------
        # ID
        # ----------------------------------------------------------------------
        $htmlArr[ "HIDDEN_ID" ] = '<input type="hidden" name="id" value="'.$dataArr[ "id" ].'" />';
        # ----------------------------------------------------------------------
        # 其它內容
        # ----------------------------------------------------------------------
        $htmlArr[ "TITLE" ] = DEVIL_APP_PROJECT_NAME;
        $htmlArr[ "PUBLIC_URL" ] = DEVIL_APP_PUBLIC_URL;
        $htmlArr[ "URL" ] = DEVIL_APP_Url;
        $htmlArr[ "UPD_URL" ] = DEVIL_APP_Url . __CLASS__ . '/index/upd/';
        # ----------------------------------------------------------------------
        # 輸出內容
        # ----------------------------------------------------------------------
        $this->My_HtmlView->Extension_HtmlView
        (
            __CLASS__."_Upd_Page" ,
            __CLASS__."_Upd.html" ,
            $htmlArr
        );
    }

    /**
    @brief      精緻餐盒( 圖片上傳程序 )
    **/
    private function _upd( )
    {
        if ( Core_IsPost() && $_POST != '' )
        {
            # ------------------------------------------------------------------
            # 檢查圖片類型
            # ------------------------------------------------------------------
            if( $_FILES[ "pictures" ][ "type" ] != 'image/jpeg' )
            {
                show_errormsg( '圖片格式錯誤，請重新上傳！！' );
            }
            // # ------------------------------------------------------------------
            // # 檢查圖片大小
            // # ------------------------------------------------------------------
            // if( $_FILES[ "pictures" ][ "size" ][ 0 ] > '524288' )
            // {
            //     show_errormsg( '圖片過大，請重新上傳！！' );
            // }
            # ------------------------------------------------------------------
            # 過濾『ID』
            # ------------------------------------------------------------------
            $id = intval( $_POST[ "id" ] );
            # ------------------------------------------------------------------
            # 上傳
            # ------------------------------------------------------------------
            if( ! move_uploaded_file( $_FILES[ "pictures" ][ "tmp_name" ], $this->imgurl.$id.'.jpg' ) )
            {
                show_errormsg( '圖片上傳失敗，請重新上傳！！' );
            }
            Core_Redirect( __CLASS__ );
        }
        Core_Redirect( __CLASS__ );
    }

    /**
    @brief      精緻餐盒( 刪除 )
    **/
    private function _del( $_id )
    {
        # ----------------------------------------------------------------------
        # 過濾『ID』
        # ----------------------------------------------------------------------
        $id = intval( $_id );
        # ----------------------------------------------------------------------
        # 防呆
        # ----------------------------------------------------------------------
        if( $id )
        {
            # ------------------------------------------------------------------
            # 執行更新
            # ------------------------------------------------------------------
            if ( Bootstart::$_lib[ 'Core_Pdo_Driver' ]->query( "DELETE FROM `product` WHERE `id` = '{$id}'" ) === FALSE )
            {
                show_errormsg( '刪除失败！' );
            }

            if( file_exists( $this->imgurl.$id.'.png' ) )
            {
                unlink( $this->imgurl.$id.'.png' );
            }
        }
        Core_Redirect( __CLASS__ );
    }
}